package ec.edu.ups.ppw63.examenloja.model;

public class Recargarecibo {
	private int codigo_recargarecibo;
	private String numero_recargarecibo;
	private int monto_recargarecibo;
	private int codigo_operador_recargarecibo;
	
	
	


	public Recargarecibo() {
		
	}


	public int getCodigo_recargarecibo() {
		return codigo_recargarecibo;
	}


	public void setCodigo_recargarecibo(int codigo_recargarecibo) {
		this.codigo_recargarecibo = codigo_recargarecibo;
	}


	public String getNumero_recargarecibo() {
		return numero_recargarecibo;
	}


	public void setNumero_recargarecibo(String numero_recargarecibo) {
		this.numero_recargarecibo = numero_recargarecibo;
	}


	public int getMonto_recargarecibo() {
		return monto_recargarecibo;
	}


	public void setMonto_recargarecibo(int monto_recargarecibo) {
		this.monto_recargarecibo = monto_recargarecibo;
	}


	public int getCodigo_operador_recargarecibo() {
		return codigo_operador_recargarecibo;
	}


	public void setCodigo_operador_recargarecibo(int codigo_operador_recargarecibo) {
		this.codigo_operador_recargarecibo = codigo_operador_recargarecibo;
	}


	@Override
	public String toString() {
		return "Recargarecibo [codigo_recargarecibo=" + codigo_recargarecibo + ", numero_recargarecibo="
				+ numero_recargarecibo + ", monto_recargarecibo=" + monto_recargarecibo
				+ ", codigo_operador_recargarecibo=" + codigo_operador_recargarecibo + "]";
	}
	
	
	
	
	
	


	
	
	
}
